    public PreferencesComboBox(String title, List<String> values, String initial) {
        initComponents();

        label.setText(title);
        values.stream().forEach((t) -> { combo.addItem(t); });
        combo.setSelectedItem(initial);
    }

    @Override
    public String getPreferenceName() {
        return getName();
    }

    @Override
    public String getPreferenceValue() {
        return combo.getSelectedItem().toString();
    }

    @Override
    public void setPreferenceValue(String value) {
        if(value != null) {
            combo.setSelectedItem(value);
        }
    }
    
