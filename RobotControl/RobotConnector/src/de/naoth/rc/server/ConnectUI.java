  /**
   * Set the controls as if we are disconnected or not
   * @param isConnected
   */
  public void showConnected(boolean isConnected)
  {
    if(isConnected)
    {
      lblStatus.setText("connected to "+ messageServer.getAddress().toString());
    }
    else
    {
      lblStatus.setText("not connected");
    }
  }
  
  
  
