    class ColorButton extends JToggleButton
  {
    private Color buttonBackgroundOn = new Color(0.7f,0.7f,0.9f);
    private Color buttonBackgroundOff = new Color(0.9f,0.9f,1.0f,0.0f);
    private Color borderColor = Color.darkGray;

    public ColorButton(Color color, String colorLabel)
    {
        setToolTipText(colorLabel);
        setActionCommand(colorLabel);
        setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0), 0));

        setFocusPainted(false);
        setPreferredSize(new java.awt.Dimension(27, 27));

        setIcon(new ColorIcon(color, buttonBackgroundOff, buttonBackgroundOff));
        setRolloverIcon(new ColorIcon(color, buttonBackgroundOff, borderColor));
        setSelectedIcon(new ColorIcon(color, buttonBackgroundOn, borderColor));
    }//end ColorButton
  }//end ColorButton


  public Colors.ColorClass getSelectedColor()
  {
      try{
        return Colors.ColorClass.valueOf(this.colorClassButtonGroup.getSelection().getActionCommand());
      }catch(IllegalArgumentException e)
      {
          return null;
      }
  }//end getSelectedColor

  class ColorIcon implements Icon
  {
    private Color color;
    private Color backGroundColor;
    private Color borderColor;

    private final int outerWidth = 26;
    private final int innerWidth = 16;

    public ColorIcon(Color color, Color backGroundColor, Color borderColor)
    {
        this.color = color;
        this.backGroundColor = backGroundColor;
        this.borderColor = borderColor;
    }

    public void paintIcon(Component c, Graphics g, int x, int y) {
        // draw outer frame
        Color old = g.getColor();
        g.setColor(backGroundColor);
        g.fillRect(x, y, outerWidth, outerWidth);
        g.setColor(borderColor);
        g.drawRect(x, y, outerWidth, outerWidth);

        // draw inner frame
        int widthOffset = (outerWidth - innerWidth)/2;
        g.setColor(color);
        g.fillRect(x+widthOffset, y+widthOffset, innerWidth, innerWidth);
        g.setColor(Color.darkGray);
        g.drawRect(x+widthOffset, y+widthOffset, innerWidth, innerWidth);

        // reset the color
        g.setColor(old);
    }//end paintIcon

    public int getIconWidth() {
        return outerWidth+1;
    }

    public int getIconHeight() {
        return outerWidth+1;
    }
  }//end class ColorIcon


